def rank_value(rank):
    if rank in ["J","Q","K"]:
        return 10
    if rank=="A":
        return 11
    return int(rank)

def create_deck():
    deck = [(s,r) for s in suits for r in ranks]
    random.shuffle(deck)
    return deck

def calculate_score(cards):
    total = 0
    ace_cnt = 0
    for (s,r) in cards:
        v = rank_value(r)
        total += v
        if r=="A":
            ace_cnt += 1
    while total>21 and ace_cnt>0:
        total -= 10
        ace_cnt -= 1
    return total

